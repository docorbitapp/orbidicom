export type I18nKey = keyof (typeof STRINGS)["en"];

export interface Locale {
  code: string;
  label: string;
}

/** Languages the viewer ships with, for the built-in language switcher. */
export const LOCALES: Locale[] = [
  { code: "en", label: "English" },
  { code: "tr", label: "Türkçe" },
  { code: "de", label: "Deutsch" },
  { code: "es", label: "Español" },
];

const current = ref("en");

export function setLang(lang: string): void {
  current.value = lang in STRINGS ? lang : "en";
}

/** The active language code (reactive: reading it in a render tracks changes). */
export function getLang(): string {
  return current.value;
}

export function t(key: I18nKey): string {
  const table = (STRINGS as Record<string, Record<string, string>>)[current.value] ?? STRINGS.en;
  return table[key] ?? STRINGS.en[key];
}
