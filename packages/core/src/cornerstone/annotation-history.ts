/**
 * Annotation undo / redo — a framework-agnostic history of the user's annotation
 * actions. Scope: **create** (a measurement was drawn) and **delete** (a
 * measurement was removed). Moving/resizing an existing annotation is *not* a
 * tracked step, but such edits survive an undo→redo round trip because undoing a
 * create snapshots the annotation's *current* state first (see {@link createAnnotationHistory}).
 *
 * The controller is pure logic over an injected {@link AnnotationStateAdapter}, so
 * it is fully testable in node. {@link startAnnotationHistory} wires the default
 * singleton ({@link annotationHistory}) to Cornerstone's global annotation events;
 * the Vue layer drives undo()/redo() and re-renders viewports afterwards.
 */
import { annotation, Enums as csToolsEnums } from "@cornerstonejs/tools";
import { eventTarget } from "@cornerstonejs/core";

/** The measurement tools whose create/delete we track (mirrors measurements.ts). */
const KNOWN_TOOLS = new Set(["Length", "Angle", "RectangleROI", "EllipticalROI", "Probe"]);

/** Minimal shape we read from a Cornerstone annotation (cloned opaquely otherwise). */
export interface HistoryAnnotation {
  annotationUID?: string;
  autoGenerated?: boolean;
  metadata?: { toolName?: string; FrameOfReferenceUID?: string };
  [k: string]: unknown;
}

/** The slice of Cornerstone's `annotation.state` the history needs (injectable for tests). */
export interface AnnotationStateAdapter {
  getAnnotation(uid: string): HistoryAnnotation | undefined;
  addAnnotation(annotation: HistoryAnnotation, groupSelector: string): void;
  removeAnnotation(uid: string): void;
}

type Entry =
  | { kind: "create"; uid: string; group: string; snapshot?: HistoryAnnotation }
  | { kind: "delete"; uid: string; group: string; snapshot: HistoryAnnotation }
  | { kind: "edit"; uid: string; group: string; before: HistoryAnnotation; after: HistoryAnnotation };

export interface AnnotationHistory {
  /** Record that the user drew a new annotation (called from the COMPLETED listener). */
  recordCreate(uid: string, groupSelector: string): void;
  /** Record that the user deleted an annotation (called from the REMOVED listener). */
  recordDelete(annotation: HistoryAnnotation): void;
  /** Capture the pre-edit snapshot for a move/resize gesture (idempotent within a gesture). */
  beginEdit(uid: string): void;
  /** Commit a gesture: push one `edit` step iff the annotation's geometry changed. */
  commitEdit(uid: string): void;
  /** Undo the most recent action; returns false if there's nothing to undo. */
  undo(): boolean;
  /** Redo the most recently undone action; returns false if there's nothing to redo. */
  redo(): boolean;
  canUndo(): boolean;
  canRedo(): boolean;
  /** Clear both stacks. Does not mutate annotation state. */
  reset(): void;
  /** Subscribe to stack changes; returns an unsubscribe fn. */
  subscribe(cb: () => void): () => void;
}

const clone = <T>(v: T): T =>
  typeof structuredClone === "function" ? structuredClone(v) : (JSON.parse(JSON.stringify(v)) as T);

/** Serialized geometry (handles) of an annotation — the signal that an edit changed it. */
const handlesKey = (a: HistoryAnnotation | undefined): string =>
  JSON.stringify((a as { data?: { handles?: unknown } } | undefined)?.data?.handles ?? null);

/**
 * Build a history controller over the given annotation-state adapter.
 *
 * The `applying` guard is essential: undo/redo mutate state via the adapter, and
 * `removeAnnotation` re-fires Cornerstone's ANNOTATION_REMOVED *synchronously*.
 * Without the guard, the REMOVED listener would record our own operation as a new
 * user delete and corrupt the stacks. While `applying` is set, recording no-ops.
 */
export function createAnnotationHistory(adapter: AnnotationStateAdapter): AnnotationHistory {
  let undoStack: Entry[] = [];
  let redoStack: Entry[] = [];
  let applying = false;
  const subs = new Set<() => void>();
  // Last *stable* (created/edited/restored) snapshot per uid, so an edit's "before"
  // is the true pre-gesture geometry rather than a mid-drag frame.
  const stable = new Map<string, HistoryAnnotation>();
  // Per-uid "before" snapshots captured at gesture start, awaiting commit.
  const pendingBefore = new Map<string, HistoryAnnotation>();
  const notify = () => {
    for (const cb of subs) cb();
  };

  function recordCreate(uid: string, groupSelector: string): void {
    if (applying || !uid) return;
    undoStack.push({ kind: "create", uid, group: groupSelector });
    redoStack = [];
    const live = adapter.getAnnotation(uid);
    if (live) stable.set(uid, clone(live)); // seed pre-edit baseline
    notify();
  }

  function recordDelete(ann: HistoryAnnotation): void {
    if (applying) return;
    const uid = ann.annotationUID ?? "";
    if (!uid) return;
    undoStack.push({
      kind: "delete",
      uid,
      group: ann.metadata?.FrameOfReferenceUID ?? "",
      snapshot: clone(ann),
    });
    redoStack = [];
    stable.delete(uid);
    pendingBefore.delete(uid);
    notify();
  }

  function beginEdit(uid: string): void {
    if (applying || !uid) return;
    if (pendingBefore.has(uid)) return; // gesture already in progress
    const before = stable.get(uid) ?? adapter.getAnnotation(uid);
    if (before) pendingBefore.set(uid, clone(before));
  }

  function commitEdit(uid: string): void {
    if (applying || !uid) return;
    const before = pendingBefore.get(uid);
    pendingBefore.delete(uid);
    if (!before) return;
    const live = adapter.getAnnotation(uid);
    if (!live) return;
    const after = clone(live);
    if (handlesKey(before) === handlesKey(after)) {
      stable.set(uid, after); // no geometry change — refresh baseline, record nothing
      return;
    }
    undoStack.push({
      kind: "edit",
      uid,
      group: before.metadata?.FrameOfReferenceUID ?? after.metadata?.FrameOfReferenceUID ?? "",
      before,
      after,
    });
    redoStack = [];
    stable.set(uid, after);
    notify();
  }

  function apply(fn: () => void): void {
    applying = true;
    try {
      fn();
    } finally {
      applying = false;
    }
  }

  function undo(): boolean {
    const entry = undoStack.pop();
    if (!entry) return false;
    apply(() => {
      if (entry.kind === "create") {
        const live = adapter.getAnnotation(entry.uid);
        if (live) entry.snapshot = clone(live);
        adapter.removeAnnotation(entry.uid);
        stable.delete(entry.uid);
      } else if (entry.kind === "delete") {
        adapter.addAnnotation(clone(entry.snapshot), entry.group);
        stable.set(entry.uid, clone(entry.snapshot));
      } else {
        // edit: restore the pre-edit snapshot (replace current state)
        adapter.removeAnnotation(entry.uid);
        adapter.addAnnotation(clone(entry.before), entry.group);
        stable.set(entry.uid, clone(entry.before));
      }
    });
    redoStack.push(entry);
    notify();
    return true;
  }

  function redo(): boolean {
    const entry = redoStack.pop();
    if (!entry) return false;
    apply(() => {
      if (entry.kind === "create") {
        if (entry.snapshot) {
          adapter.addAnnotation(clone(entry.snapshot), entry.group);
          stable.set(entry.uid, clone(entry.snapshot));
        }
      } else if (entry.kind === "delete") {
        adapter.removeAnnotation(entry.uid);
        stable.delete(entry.uid);
      } else {
        adapter.removeAnnotation(entry.uid);
        adapter.addAnnotation(clone(entry.after), entry.group);
        stable.set(entry.uid, clone(entry.after));
      }
    });
    undoStack.push(entry);
    notify();
    return true;
  }

  function reset(): void {
    undoStack = [];
    redoStack = [];
    stable.clear();
    pendingBefore.clear();
    notify();
  }

  return {
    recordCreate,
    recordDelete,
    beginEdit,
    commitEdit,
    undo,
    redo,
    canUndo: () => undoStack.length > 0,
    canRedo: () => redoStack.length > 0,
    reset,
    subscribe: (cb) => {
      subs.add(cb);
      return () => {
        subs.delete(cb);
      };
    },
  };
}

/* ------------------------------------------------------------------ *
 * Default singleton, wired to Cornerstone's global annotation events.
 * ------------------------------------------------------------------ */

const realAdapter: AnnotationStateAdapter = {
  getAnnotation: (uid) =>
    annotation.state.getAnnotation(uid) as unknown as HistoryAnnotation | undefined,
  addAnnotation: (ann, group) => {
    annotation.state.addAnnotation(ann as never, group as never);
  },
  removeAnnotation: (uid) => {
    annotation.state.removeAnnotation(uid);
  },
};

/** The viewer-wide annotation history (Cornerstone annotation state is global). */
export const annotationHistory = createAnnotationHistory(realAdapter);

const annotationOf = (evt: Event): HistoryAnnotation | undefined =>
  (evt as CustomEvent<{ annotation?: HistoryAnnotation }>).detail?.annotation;

const isTracked = (a: HistoryAnnotation | undefined): a is HistoryAnnotation =>
  !!a && KNOWN_TOOLS.has(a.metadata?.toolName ?? "") && a.autoGenerated !== true;

const onCompleted = (evt: Event) => {
  const a = annotationOf(evt);
  if (isTracked(a)) {
    annotationHistory.recordCreate(a.annotationUID ?? "", a.metadata?.FrameOfReferenceUID ?? "");
  }
};

const onRemoved = (evt: Event) => {
  const a = annotationOf(evt);
  if (isTracked(a)) annotationHistory.recordDelete(a);
};

let started = false;

/**
 * Attach the singleton {@link annotationHistory} to Cornerstone's ANNOTATION_COMPLETED
 * / ANNOTATION_REMOVED events. Idempotent; returns a teardown that also detaches.
 */
export function startAnnotationHistory(): () => void {
  if (!started) {
    eventTarget.addEventListener(
      csToolsEnums.Events.ANNOTATION_COMPLETED,
      onCompleted as EventListener,
    );
    eventTarget.addEventListener(
      csToolsEnums.Events.ANNOTATION_REMOVED,
      onRemoved as EventListener,
    );
    started = true;
  }
  return () => {
    eventTarget.removeEventListener(
      csToolsEnums.Events.ANNOTATION_COMPLETED,
      onCompleted as EventListener,
    );
    eventTarget.removeEventListener(
      csToolsEnums.Events.ANNOTATION_REMOVED,
      onRemoved as EventListener,
    );
    started = false;
  };
}
